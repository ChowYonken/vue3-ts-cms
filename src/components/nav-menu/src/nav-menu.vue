<template>
  <div class="nav-menu">
    <!-- 标题 -->
    <div class="logo">
      <img class="img" src="~@/assets/img/logo.svg" alt="logo" />
      <span class="title" v-if="!collapse">后台管理系统</span>
    </div>
    <el-menu
      :default-active="defaultActive"
      class="el-menu-vertical"
      :collapse="collapse"
      :collapse-transition="false"
      background-color="#0c2135"
      text-color="#b7bdc3"
      active-text-color="#0a60bd"
    >
      <template v-for="item in userMenus" :key="item.id">
        <!-- 二级菜单 -->
        <template v-if="item.type === 1">
          <el-sub-menu :index="item.id + ''">
            <template #title>
              <!-- 菜单图标 -->
              <component
                v-if="item.icon"
                class="icons"
                :is="item.icon.slice(8)"
              ></component>
              <!-- 菜单名 -->
              <span>{{ item.name }}</span>
            </template>
            <!-- 遍历二级菜单里的children  -->
            <template v-for="subitem in item.children" :key="subitem.id">
              <el-menu-item
                :index="subitem.id + ''"
                @click="handleMenuItemClick(subitem)"
              >
                <component
                  v-if="subitem.icon"
                  class="icons"
                  :is="subitem.icon.slice(8)"
                ></component>
                <span>{{ subitem.name }}</span>
              </el-menu-item>
            </template>
          </el-sub-menu>
        </template>
        <!-- 一级菜单 -->
        <template v-else-if="item.type === 2">
          <el-menu-item
            :index="item.id + ''"
            @click="handleMenuItemClick(item)"
          >
            <!-- 菜单图标 -->
            <component
              v-if="item.icon"
              class="icons"
              :is="item.icon.slice(8)"
            ></component>
            <!-- 菜单名 -->
            <span>{{ item.name }}</span>
          </el-menu-item>
        </template>
      </template>
    </el-menu>
  </div>
</template>

<script lang="ts">
import { defineComponent, computed, ref } from 'vue'
import { useStore } from '@/store'
import { useRoute, useRouter } from 'vue-router'
import { mapMenuId } from '@/utils/map-menus'

export default defineComponent({
  props: {
    collapse: {
      type: Boolean,
      default: false
    }
  },
  setup() {
    const store = useStore()
    const userMenus = computed(() => store.state.login.userMenus)

    const router = useRouter()
    const route = useRoute()
    const currentPath = route.path

    const menu = mapMenuId(userMenus.value, currentPath)
    const defaultActive = ref(menu.id + '')

    const handleMenuItemClick = (item: any) => {
      router.push({ path: item.url ?? '/not-found' })
    }

    return { userMenus, defaultActive, handleMenuItemClick }
  }
})
</script>

